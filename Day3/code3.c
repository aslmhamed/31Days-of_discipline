// C Program to Illustrate Array Indexing in C
#include <stdio.h>
 
int main()
{
 
    // creating integer array with 2 elements
    int arr[2] = { 10, 20 };
 
    // printing element at index 1
    printf("Array[1]: %d\n", arr[1]);
 
    // printing element at index 0
    printf("Array[0]: %d", arr[0]);
   
    return 0;
}
